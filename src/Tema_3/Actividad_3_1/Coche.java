package Tema_3.Actividad_3_1;

public class Coche extends Vehiculo{
    Integer numPuertas;
    Integer numPlazas;
    Boolean remoloque;

    public Integer getNumPuertas() {
        return numPuertas;
    }

    public void setNumPuertas(Integer numPuertas) {
        this.numPuertas = numPuertas;
    }

    public Integer getNumPlazas() {
        return numPlazas;
    }

    public void setNumPlazas(Integer numPlazas) {
        this.numPlazas = numPlazas;
    }

    public Boolean getRemoloque() {
        return remoloque;
    }

    public void setRemoloque(Boolean remoloque) {
        this.remoloque = remoloque;
    }
}
